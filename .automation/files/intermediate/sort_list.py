def bubble_sort(lst):
    n = len(lst)
    for i in range(n):
        for j in range(0, n - i - 1):
            if lst[j] > lst[j + 1]:
                lst[j], lst[j + 1] = lst[j + 1], lst[j]
    return lst

def selection_sort(lst):
    n = len(lst)
    for i in range(n):
        min_idx = i
        for j in range(i + 1, n):
            if lst[j] < lst[min_idx]:
                min_idx = j
        lst[i], lst[min_idx] = lst[min_idx], lst[i]
    return lst

def insertion_sort(lst):
    for i in range(1, len(lst)):
        key = lst[i]
        j = i - 1
        while j >= 0 and lst[j] > key:
            lst[j + 1] = lst[j]
            j -= 1
        lst[j + 1] = key
    return lst

numbers = [64, 34, 25, 12, 22, 11, 90]
print(f"Original: {numbers}")

bubble_result = bubble_sort(numbers.copy())
selection_result = selection_sort(numbers.copy())
insertion_result = insertion_sort(numbers.copy())

print(f"Bubble sort: {bubble_result}")
print(f"Selection sort: {selection_result}")
print(f"Insertion sort: {insertion_result}")
